import type { Asset } from '@imagen-ps/core-engine';

/**
 * 创建合成 Asset[] 用于 mock provider 成功路径。
 *
 * 返回的 asset 使用真实可解码的透明 PNG，避免 Photoshop
 * 写回 smoke 因半有效图片 payload 触发 host 崩溃。
 */
export function createSyntheticAssets(count = 1): Asset[] {
  // 1x1 透明 PNG，IDAT 解压后是完整的 filter byte + RGBA 像素。
  const placeholderBytes = new Uint8Array([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,
    0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4,
    0x89, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x44, 0x41,
    0x54, 0x78, 0x9c, 0x63, 0x60, 0x60, 0x60, 0x60,
    0x00, 0x00, 0x00, 0x05, 0x00, 0x01, 0xa5, 0xf6,
    0x45, 0x40, 0x00,
    0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae,
    0x42, 0x60, 0x82,
  ]);

  return Array.from({ length: count }, (_, i) => ({
    type: 'image' as const,
    name: `mock-image-${i + 1}.png`,
    mimeType: 'image/png',
    data: placeholderBytes,
  }));
}
